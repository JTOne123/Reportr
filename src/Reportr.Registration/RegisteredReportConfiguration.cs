namespace Reportr.Registration
{
    /// <summary>
    /// Represents configuration details for a registered report
    /// </summary>
    public class RegisteredReportConfiguration
    {
        /// <summary>
        /// Gets or sets the report name
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Gets or sets the report title
        /// </summary>
        public string Title { get; set; }

        /// <summary>
        /// Gets or sets the report description
        /// </summary>
        public string Description { get; set; }
    }
}
